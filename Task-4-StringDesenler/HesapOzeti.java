public class HesapOzeti {

    public static void main(String[] args) {

        //Hesap Ozeti
        // Başlıklar
        System.out.println("Ürün\tMiktar\tFiyat");
        System.out.println("------------------------");

        // Ürünler

        System.out.println("Elma     \t2\t10");
        System.out.println("Ekmek     \t1\t5");
        System.out.println("Süt       \t3\t7");
        System.out.println("Çikolata\t5\t4");
        System.out.println("\nHesap: " + (2*10 + 1*5 + 3*7 + 5*4) + " TL ");

    }
}
